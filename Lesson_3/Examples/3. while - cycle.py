# Цикл окончен

'''
Цикл while может быть прерван оператором break, цикл будет завершен, и поток
программы будет продолжен без выполнения действий из else.

while <условие>:
   if <Условие>:
        <действие>
        break # прерывает цикл и выходит из него
    else:
        <действие>

Оператор continue прерывает текущение выполнение итерации и переходит 
к следующему элементу <последовательности>:

while <условие>:
   if <Условие>:
        <действие>
        continue # Прерывает текущую итерацию и переходит к следующему элементу 
    else:
        <действие>

Обычно фразы break и continue в pyton связаны с условными операторами.

'''

# Пример  c break
a = 0
while a < 10:
    if a != 6:
        print(a)
        a += 1
    else:
        print('Дошел до 6. Цикл остановлен')
        break  # Прерывает цикл

# Пример  c continue
a = 0
while a < 10:
    if a != 6:
        print(a)
        a += 1
    else:
        a += 1
        continue  # Пропустит 6 и перейдет к следующему элементу

'''
Бесконечный цикл while в Python
Бесконечный цикл while — это цикл, в котором условие никогда не становится 
ложным. Это значит, что тело исполняется снова и снова, а цикл никогда не 
заканчивается.
Типичный пример: 

while True:
    <выражение>
    
цикл не завершится, пока мы его не остановим вручную (ctrl + C). 
Всегда предусматривайте условия выхода из цикла, иначе программа будет 
бесконечно расходовать оперативную память компьютера, что в конце концов
приведет к зависанию системы.
'''
# Бесконечный цикл
while True:
    b = input('Как тебя зовут?')
    if b!='stop': #условие выхода
        print('Привет', b, ', Добро пожаловать!')
    else:
        break
