""" Урок 2 - 3. Цикл while"""

'''
Цикл while используется в Python для неоднократного исполнения определенной
инструкции до тех пор, пока заданное условие остается истинным. Этот цикл
позволяет программе перебирать блок кода.

while test_expression:
    body of while

Сначала программа оценивает условие цикла while. Если оно истинное, начинается
цикл, и тело while исполняется. Тело будет исполняться до тех пор, пока условие
остается истинным. Если оно становится ложным, программа выходит из цикла и
прекращает исполнение тела.
'''

# Пример:

a = 1

while a < 10:  # пока a будет меньше 10 будет выполняться тело цикла
    print('Цикл выполнился', a, 'раз(а)')
    a = a + 1
print('Цикл окончен')

